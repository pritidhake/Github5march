public class operatersHW {
    public static void main(String[] args){
        //Ternary operaters
        int a = 7;
        int b = 9;
        int min = a<b?a:b;
        int max = a>b?a:b;
        System.out.println("minimum " +min);
        System.out.println("maximum " +max);
        //EvenOdd Numbers
        int c =7;
        if (c%2==0){
            System.out.println(c +" is Even");
        }
        else{
            System.out.println(c+" is odd");
        }
    }
}








